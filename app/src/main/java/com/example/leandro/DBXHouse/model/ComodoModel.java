package com.example.leandro.DBXHouse.model;

public class ComodoModel {

    private Integer idComodo;
    private String nomeComodo;
    private Integer metrosQuadrados;

    public Integer getIdComodo() {
        return idComodo;
    }

    public void setIdComodo(Integer idComodo) {
        this.idComodo = idComodo;
    }

    public String getNomeComodo() {
        return nomeComodo;
    }

    public void setNomeComodo(String nomeComodo) {
        this.nomeComodo = nomeComodo;
    }

    public Integer getMetrosQuadrados() {
        return metrosQuadrados;
    }

    public void setMetrosQuadrados(Integer metrosQuadrados) {
        this.metrosQuadrados = metrosQuadrados;
    }
}
